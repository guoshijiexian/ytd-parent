package com.ytd.pojo;

public class UserInfo {
    private Byte userid;

    private Byte email;

    private Byte nickname;

    private Byte realname;

    private Byte phonenm;

    private Byte registertime;

    private Byte totalamount;

    private Byte times;

    private Byte birth;

    private Byte age;

    private Byte sex;

    public Byte getUserid() {
        return userid;
    }

    public void setUserid(Byte userid) {
        this.userid = userid;
    }

    public Byte getEmail() {
        return email;
    }

    public void setEmail(Byte email) {
        this.email = email;
    }

    public Byte getNickname() {
        return nickname;
    }

    public void setNickname(Byte nickname) {
        this.nickname = nickname;
    }

    public Byte getRealname() {
        return realname;
    }

    public void setRealname(Byte realname) {
        this.realname = realname;
    }

    public Byte getPhonenm() {
        return phonenm;
    }

    public void setPhonenm(Byte phonenm) {
        this.phonenm = phonenm;
    }

    public Byte getRegistertime() {
        return registertime;
    }

    public void setRegistertime(Byte registertime) {
        this.registertime = registertime;
    }

    public Byte getTotalamount() {
        return totalamount;
    }

    public void setTotalamount(Byte totalamount) {
        this.totalamount = totalamount;
    }

    public Byte getTimes() {
        return times;
    }

    public void setTimes(Byte times) {
        this.times = times;
    }

    public Byte getBirth() {
        return birth;
    }

    public void setBirth(Byte birth) {
        this.birth = birth;
    }

    public Byte getAge() {
        return age;
    }

    public void setAge(Byte age) {
        this.age = age;
    }

    public Byte getSex() {
        return sex;
    }

    public void setSex(Byte sex) {
        this.sex = sex;
    }
}